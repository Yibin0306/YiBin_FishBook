# 乘法节点的反向传播
"""
加法的反向传播只是将上游的值传给下游，并不需要正向传播的输入信号。
但是，乘法的反向传播需要正向传播时的输入信号值。因此，实现乘法节点的反向传播时，要保存正向传播的输入信号。
"""

"""
## 核心概念总结
1. **加法节点特性**：反向传播时将上游梯度值**原样分发**给所有输入节点
2. **乘法节点特性**：反向传播时需要**正向传播的输入值**进行交叉相乘计算
3. **核心差异**：
   - 加法节点反向传播不依赖前向输入值
   - 乘法节点反向传播需要前向输入值作为计算因子

## 数学原理扩充
### 加法节点 (z = x + y)
- 局部导数：∂z/∂x = 1, ∂z/∂y = 1
- 反向传播公式：
  ∇x = ∇z * 1 = ∇z
  ∇y = ∇z * 1 = ∇z
- **梯度特性**：梯度均等分配，无信息损失

### 乘法节点 (z = x * y)
- 局部导数：∂z/∂x = y, ∂z/∂y = x
- 反向传播公式：
  ∇x = ∇z * y
  ∇y = ∇z * x
- **梯度特性**：梯度值与输入信号交叉相乘
"""