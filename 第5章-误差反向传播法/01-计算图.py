# 用计算图求解
"""
正向传播 vs 反向传播
---------------------
| 特性         | 正向传播                     | 反向传播                     |
|--------------|-----------------------------|-----------------------------|
| 方向         | 输入层 → 输出层              | 输出层 → 输入层              |
| 主要目的     | 计算预测结果和损失           | 计算参数梯度                |
| 计算复杂度   | O(N)                       | O(N) (与正向传播同阶)       |
| 核心操作     | 矩阵乘法 + 激活函数          | 链式求导 + 矩阵转置乘法      |
| 输出         | 预测值 y_pred 和损失 L      | 梯度 ∂L/∂W 和 ∂L/∂b        |
"""

