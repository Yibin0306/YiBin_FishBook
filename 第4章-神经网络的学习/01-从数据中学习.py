# 2. 端到端学习（End-to-End）
# =============================
"""
关键特性：
- 直接从原始输入(文本/图像等)学习到最终输出
- 无需人工设计特征或中间处理步骤
- 神经网络通过层次结构自动发现数据中的复杂模式
"""

# 训练数据和测试数据
"""
一般将数据分为训练数据和测试数据两部分来进行学习和实验等。
首先，使用训练数据进行学习，寻找最优的参数；然后，使用测试数据评价训练得到的模型的实际能力。
为了正确评价模型的泛化能力，就必须划分训练数据和测试数据。另外，训练数据也可以称为监督数据。
泛化能力是指处理未被观察过的数据（不包含在训练数据中的数据）的能力。
只对某个数据集过度拟合的状态称为过拟合（over fitting）
"""

# =================================
# 3. 模型评估与泛化能力
# =================================
# 数据划分原则：
#   - 训练集：用于模型学习，调整参数权重
#   - 测试集：用于评估模型在新数据上的表现

# 关键概念说明：
#   - 泛化能力：模型处理未见过数据的能力（核心目标）
#   - 过拟合：模型在训练数据上表现完美，但在新数据上表现差
#       原因：过度记忆训练数据细节和噪声

# 实践意义：
#   - 严格分离训练/测试数据是可靠评估的基础
#   - 防止测试数据污染训练过程
#   - 过拟合检测：训练精度高但测试精度低